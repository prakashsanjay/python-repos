course = 'Python For Beginners'
print (len(course))    # to count the no of characters in the string
print (course.upper()) # display the string in upper case
print (course.lower()) # display the string in lower case
print (course)         # display the original string
print (course.find('F')) # display the index of letter F in the string
print (course.replace('Beginners' ,'Absolute Beginners'))
 # above method is to replace a character in the string
print ('Python' in course) # display boolean value true/false
